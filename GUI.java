    private void rentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rentButtonActionPerformed
        if(this.roomNumberTextLabel.getText().isEmpty() || 
                this.ownerNameTextLabel.getText().isEmpty() || 
                this.rentTimeTextLabel.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "You must show all fields");
        } else {
            int roomNumber = Integer.parseInt(this.roomNumberTextLabel.getText());
            String currentOwner = this.ownerNameTextLabel.getText();
            String rentTime = this.rentTimeTextLabel.getText();
            roomsByNumber.get(roomNumber).setCurrentOwner(currentOwner);
            roomsByNumber.get(roomNumber).setTimeLeft(rentTime);
            
        }
    }//GEN-LAST:event_rentButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if(this.newRoomNumberTextField.getText().isEmpty() ||
                this.roomTypeTextField.getText().isEmpty() ||
                this.roomViewTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Must fill all fiels");
            
        } else {
             int roomNumber = Integer.parseInt(this.newRoomNumberTextField.getText());
             String type = this.roomTypeTextField.getText();
             String view = this.roomViewTextField.getText();
             
             Room room = new Room(roomNumber, type, view);
             rooms.add(room);
             roomsByNumber.put(room.getNumber(), room);
             
             this.roomFile.write(""+room.getNumber());
             this.typeFile.write(room.getType());
             this.viewFile.write(room.getView());
             if(room.isIsAvailable()) {
                 this.isAvailableFile.write("true");
             } else {
                 this.isAvailableFile.write("false");
             }
             this.currentOwnerFile.write(room.getCurrentOwner());
             this.timeLeftFile.write(room.getTimeLeft());
             this.roomsTextArea.setText(this.roomsTextArea.getText()+"\n" + room.getNumber() +
                     " : " + room.getType()+" , " +room.getView()+ " , "+room.getCurrentOwner()+ " , "+
                     room.getTimeLeft()+ " , "+"true");
        }
    }//GEN-LAST:event_addButtonActionPerformed

