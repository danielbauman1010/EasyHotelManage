    public GUI() {
        initComponents();
        
        try {
            
            roomFile = new FileManager("rooms.txt");
            viewFile = new FileManager("views");
            isAvailableFile = new FileManager("isAvailable.txt");
            typeFile = new FileManager("types.txt");
            currentOwnerFile = new FileManager("currentOwners.txt");
            timeLeftFile = new FileManager("timeLefts.txt");
            
            List<String> roomNumbersString = roomFile.readFile();
            List<String> views = viewFile.readFile();
            List<String> types = typeFile.readFile();
            List<String> isAvailablesString = isAvailableFile.readFile();
            List<String> currentOwners = currentOwnerFile.readFile();
            List<Integer> roomNumbers = new ArrayList();
            List<Boolean> isAvailables = new ArrayList();
            List<String> timeLefts = timeLeftFile.readFile();
            
            for(String roomNumberString: roomNumbersString) {
                int roomNumber = Integer.parseInt(roomNumberString);
                roomNumbers.add(roomNumber);                
            }
            for(String isAvailableString: isAvailablesString) {
                if(isAvailableString.equals("true")) {
                    isAvailables.add(true);
                } else {
                    isAvailables.add(false);
                }
            }
            
//            rooms = new ArrayList();
            for(int i = 0; i < roomNumbers.size(); i++) {
                
                int roomNumber = roomNumbers.get(i);
                String type = types.get(i);
                String view = views.get(i);
                boolean isAvailable = isAvailables.get(i);
                String currentOwner = currentOwners.get(i);
                String timeLeft = timeLefts.get(i);
                Room room = new Room(roomNumber, type, view, currentOwner, timeLeft, isAvailable);
                rooms.add(room);
            }
            for (Room r: rooms) {
                roomsByNumber.put(r.getNumber(), r);
            }
    }   catch (IOException ex) {
            Logger.getLogger(GUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
